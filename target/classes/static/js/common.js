/**
 * Returns the url parameter value by name.
 * @param name the url parameter name
 * @returns the url parameter value
 */
$.urlParam = function(name){
    var results = new RegExp('[\?&]' + name + '=([^&#]*)').exec(window.location.href);
    if (results==null){
        return null;
    }
    else{
        return results[1] || 0;
    }
};
